package de.dis2011.data;

/**
 *
 * @author aossa
 */
public class Apartment {

    private int Floor;
    private int Rent;
    private int Rooms;

    public int getFloor() {
        return Floor;
    }

    public void setFloor(int Floor) {
        this.Floor = Floor;
    }

    public int getRent() {
        return Rent;
    }

    public void setRent(int Rent) {
        this.Rent = Rent;
    }

    public int getRooms() {
        return Rooms;
    }

    public void setRooms(int Rooms) {
        this.Rooms = Rooms;
    }
}
